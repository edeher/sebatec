package com.sebatec.modelo;

/**
 *
 * @author Mi Laptop
 */
public enum Estados {
    A("Activo"),
    N("Noactivo"),
    E("Enespera");
    
    private String nom;

    private Estados(String nom) {
        this.nom = nom;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
}
