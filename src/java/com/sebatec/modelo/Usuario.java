package com.sebatec.modelo;

/**
 *
 * @author Mi Laptop
 */
public class Usuario {
   private int  idUsuario;
   private Persona persona;
   private String usuario;
   private String password;
   private String tipo;
   
    /**
     * @return the idUsuario
     */
    public int getIdUsuario() {
        return idUsuario;
    }

    /**
     * @param idUsuario the idUsuario to set
     */
    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    /**
     * @return the persona
     */
    public Persona getPersona() {
        return persona;
    }

    /**
     * @param persona the persona to set
     */
    public void setPersona(Persona persona) {
        this.persona = persona;
    }

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the estado
     */
    

    
    public Usuario() {
    }

    public Usuario(int idUsuario, Persona persona, String usuario, String password, String tipo) {
        this.idUsuario = idUsuario;
        this.persona = persona;
        this.usuario = usuario;
        this.password = password;
        this.tipo = tipo;
    }

    @Override
    public String toString() {
        return "Usuario{" + "idUsuario=" + idUsuario + ", persona=" + persona + ", usuario=" + usuario + ", password=" + password + ", tipo=" + tipo  + '}';
    }

   
    
    
}
